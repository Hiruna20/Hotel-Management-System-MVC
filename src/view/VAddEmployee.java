    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
        // TODO add your handling code here:
        try{
            String id = txt_Emp_ID.getText();
        
            CAddEmployee cae = new CAddEmployee();
            ResultSet rst = cae.SearchEmployee(id);

            DefaultTableModel model = new DefaultTableModel();
                model.addColumn("ID");
                model.addColumn("Name");
                model.addColumn("Gender");
                model.addColumn("Position");
                model.addColumn("Phone");
                model.addColumn("Email");

                boolean hasData = false;
                while (rst.next()) {
                    hasData = true;
                    Vector<String> row = new Vector<>();
                    row.add(rst.getString("empid"));
                    row.add(rst.getString("name"));
                    row.add(rst.getString("gender"));
                    row.add(rst.getString("position"));
                    row.add(rst.getString("phone"));
                    row.add(rst.getString("email"));
                    model.addRow(row);
                }
             
            if(!hasData){
                JOptionPane.showMessageDialog(this, "Incorrect Employee ID!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                tbl_Employee.setModel(model);
            }
        }
        catch(Exception e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_SearchActionPerformed

