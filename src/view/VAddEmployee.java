    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        try{
           String id, name, age, gender, position, salary, phone, email, password;
           char [] passwd;
           
           id = txt_ID.getText();
           name = txt_Name.getText();
           age = txt_Age.getText();
           
           if(radio_Male.isSelected()){
               gender = "Male";
           }
           else{
               gender = "Female";
           }
           
           position = cmb_Position.getSelectedItem().toString();
           salary = txt_Salary.getText();
           phone = txt_Phone.getText();
           email = txt_Email.getText();
           passwd = pwd_Password.getPassword();
           password = new String(passwd);
           
           CAddEmployee cae = new CAddEmployee();
           cae.AddEmployee(id, name, age, gender, position, salary, phone, email, password);
        }
        catch(Exception e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_SaveActionPerformed

