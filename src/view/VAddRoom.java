    private void btn_U_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_U_SearchActionPerformed
        // TODO add your handling code here:
        String roomNo = txt_U_Room_No.getText();
        
        CRoomAdd cra = new CRoomAdd();
        ResultSet rst = cra.SearchRoom(roomNo);
        
        DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Room No");
            model.addColumn("Availability");
            model.addColumn("Room Type");
            model.addColumn("Status");
            
            boolean hasData = false;
        try {
            while (rst.next()) {
                hasData = true;
                Vector<String> row = new Vector<>();
                row.add(rst.getString("roomNo"));
                row.add(rst.getString("availability"));
                row.add(rst.getString("roomType"));
                row.add(rst.getString("status"));
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(VAddRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
             
            if(!hasData){
                JOptionPane.showMessageDialog(this, "Incorect room No!.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                tbl_Room.setModel(model);
            }
    }//GEN-LAST:event_btn_U_SearchActionPerformed

