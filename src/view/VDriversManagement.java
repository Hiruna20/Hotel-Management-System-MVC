    private void btn_AddDriverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddDriverActionPerformed
        // TODO add your handling code here:
        try{
            String id, name, age, gender, carCompany, carModel,availability, location;
            int capasity;
            
            id = txt_A_Driver_ID.getText();
            name = txt_A_DriverName.getText();
            age = txt_A_Age.getText();
            if(radio_A_Male.isSelected()){
                gender = "Male";
            }
            else{
                gender = "Female";
            }
            carCompany = txt_A_CarCompany.getText();
            carModel = txt_A_CarModel.getText();
            availability = cmb_A_Avalability.getSelectedItem().toString();
            location = txt_A_Location.getText();
            capasity = (int) spn_A_Capacity.getValue();
            
            CDriverManagement cdm = new CDriverManagement();
            cdm.AddDriver(id, name, age, gender, carCompany, carModel, availability, location, capasity);
        }
        catch(Exception e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_AddDriverActionPerformed

