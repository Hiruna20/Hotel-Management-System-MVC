    private void btn_U_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_U_SearchActionPerformed
        // TODO add your handling code here:
        try{
            String id = txt_U_DriverID.getText();
        
            CDriverManagement cdm = new CDriverManagement();
            ResultSet rst = cdm.SearchDriver(id);

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Name");
            model.addColumn("Car Campany");
            model.addColumn("Car Model");
            model.addColumn("Availability");
            model.addColumn("Location");
            
            boolean hasData = false;
            
            while (rst.next()) {
                hasData = true;
                Vector<String> row = new Vector<>();
                row.add(rst.getString("name"));
                row.add(rst.getString("carCampany"));
                row.add(rst.getString("carModel"));
                row.add(rst.getString("availability"));
                row.add(rst.getString("location"));
                model.addRow(row);
            }
            if(!hasData){
                JOptionPane.showMessageDialog(this, "Incorrect Driver ID!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                tbl_driver.setModel(model);
            }
        }
        catch(Exception e){
            System.err.println(e.getMessage());
        }  
    }//GEN-LAST:event_btn_U_SearchActionPerformed

